package com.mikey.aop.sorting.enumerations;

/**
 * An enumeration of different states for the marking of an element.
 * @author Michael Nicholson
 */
public enum MarkingState {

    /**
     * Element is not marked.
     */
    NOT_MARKED,

    /**
     * Element is not marked with a custom colour.
     */
    CUSTOM_MARKED,

    /**
     * Element is not marked with the default colour.
     */
    DEFAULT_MARKED

}
